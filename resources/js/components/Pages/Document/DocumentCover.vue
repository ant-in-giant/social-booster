      <h2>紐付けの意味</h2>
      <p>ニーズに対して上位のレイヤーであるアイデアとの紐付けは提案のようなものです。</p>
      <p>「街を綺麗にしたい」というニーズに対して、アイデアである「ゴミ拾いをする」が提案されます。</p>
      <p>この「ゴミ拾いをする」を上位に紐付けるとプロジェクトになります。</p>
      <p>プロジェクトでは、いつゴミ拾いをするのか、どこでやるのかとより具体的になります。</p>
      <p>更に上位に紐付けるとコミュニティになります。</p>
      <p>コミュニティが「ゴミ拾いをする」アイデアを具体化したプロジェクトと紐付けられると、紐付けられたコミュニティがそのプロジェクトの実行者となります。</p>
      <p>このように、上位に対する紐付けは問題を解決するための流れだと言えます。</p>
      <p>逆に下位への紐付けやその確認は、上位のコンセプトレイヤーに本当に意義があるのかを調べる流れです。</p>
      <p>ストレスやニーズレイヤーと紐付きの少ないアイデア、プロジェクトがあれば、それは社会的意義のないコンセプトであり、そこに重きを置くコミュニティがあればそのコミュニティは社会に貢献していないことを表します。</p>
