      <p>ニーズは複数のアイデアと紐付ける事ができ、複数のストレスとも紐付け出来ます。</p>
      <h2>紐付けをするには</h2>
      <p>紐付けはコンセプトの下部右下にある上下の矢印から出来ます。</p>
      <p>上矢印が上位に対する紐付けであり、下矢印が下位に対する紐付けです。</p>
